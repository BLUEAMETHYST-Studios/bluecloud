fun asciiArt(): String {
    val title = """
        @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
        @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
        @@@@@@@@@@@@@%@@@@@@@@@@@*=%@@@@@@@@@@@%@@@@@@@@@@@
        @@@@@%=#@@@@#=*@@@@@%###-.-+#%##%@@@@@*+#%@@%+#@@@@
        @@@@%#=+#%@@%%%@%*-=*#%*:-*#+%%#*==*%@@#@@@%*=*%@@@
        @@@@@@#%@@%%%%+:++#++*%@%==*@@%++*%=+-#%@@@@@%@@@@@
        @@@@@@@@%%@%+--+#@%@#=#@@@@@@%@@@%%@#===*%%#@#%@@@@
        @@@@@@#%@%+-#*+@%-#@%*%@@@@@@@@#@@##@#+#*=#%@%@@@@@
        @@@@@@@@%=+#-%@+-:+*@%=::::#@@@@%*-=*%@#=#=*%%@@@@@
        @@@@@##%+-+#@++@%*#@-....:--*%@@@@@@@%@@@*+=#%*@@@@
        @@@@@@%-=#+@@%#%%%%%..::=+++**:..:=#%@@@%%+%+#%@@@@
        @@@@@@+-%+%@@%=....:===+++++++:.:=+###@@@@#+%+%#*@@
        @@@%%%+#=%@@%:..:-=+*++++-+++=-++++++*#@@@@#=*%%%@@
        @@@%%#=%#%=--..-+++*#*+=..+##+++=:.=++*###%@#+*%%@@
        @@@@#-=@#:..:--++++++=:...+###*++-+*+==++*##%@+*%@@
        @@@@%++@+..-++++:.=++=:...+####*+==+=:.=+*##%@*#%@@
        @@@@%#+@*::+++++==*++=--::+#*##*=-=++++++*#*#@#%%@@
        @@@@%%+*@#=+#*********+-::+#*###*******##**%@*%%@@@
        @@@@@%#-##@%%%%@@@@@@%*=::+#*#%@@@@%%%%%%%@##*%##@@
        @@@@@%%*+%=%%+#%#@@@@@#+::+#*%@@@@#@@@#=@%#@#%%#@@@
        @@@@@@%%*=#%%#%@*-#@@%@@-:+#*@%@@#-+%@%#@%%*%%%@@@@
        @@@@@@#%%#=#**%@*=#@@%@@%=+*%@%%@#-+%@%###*%%%%@@@@
        @@@%+@@@%%%==#-#@@@@:-*@@@@@@#:-#@@@@**%*#%%@@@@@@@
        @@@@@@#%@%%%%=-#%#+*@%@@@@@%@@@%%*+%@#*#%%@%@#%@@@@
        @@@@#+-=#%%%%%%#+=*%#**@%-=#@%#*#%*+*#%%%@@%*-+#%@@
        @@@@@@*%@@@@*=*%%%%**++=:.=+##***#%%%@%#+%@@@*%@@@@
        @@@@@@@@@@@@%#%@@@@@%%@@#-++%@%%%@@@@@@@#@@@@@@@@@@
        @@@@@@@@@@@@@@@@@@@@@@@@@%%@@@@@@@@@@@@@@@@@@@@@@@@   
""".trim('\n')
    return buildString {
        for (line in title.lineSequence()) {
            val lerp = Oklab.interpolator {
                stop(SRGB("#e74856"))
                stop(SRGB("#9648e7"))
            }.sequence(line.length)
            line.asSequence().zip(lerp).forEach { (c, color) ->
                append(TextColors.color(color)(c.toString()))
            }
            append("\n")
        }
    }
}